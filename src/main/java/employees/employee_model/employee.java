package employees.employee_model;

public class employee {
    private String Hotel_id;
    private String salary;
    private String working_hour;
    private String position;
    private String working_days;
    private String payroll;
    private String firstname;
    private String lastname;
    private String EmployeeID;

    public String getHotel_id(){return Hotel_id;}
    public void setHotel_id(String Hotel_id){this.Hotel_id=Hotel_id;}
    public String getSalary(){return salary;}
    public void setSalary(String salary){this.salary=salary;}
    public String getWorking_hour(){return working_hour;}
    public void setWorking_hour(String working_hour){this.working_hour=working_hour;}
    public String getPosition(){return position;}
    public void setPosition(String position){this.position=position;}
    public String getWorking_days(){return working_days;}
    public void setWorking_days(String working_days){this.working_days=working_days;}
    public String getPayroll(){return payroll;}
    public void setPayroll(String payroll){this.payroll=payroll;}
    public String getFirstname(){return firstname;}
    public void setFirstname(String firstname){this.firstname=firstname;}
    public String getLastname(){return lastname;}
    public void setLastname(String lastname){this.lastname=lastname;}
    public String getEmployeeID(){return EmployeeID;}
    public void setEmployeeID(String EmployeeID){this.EmployeeID=EmployeeID;}
}
